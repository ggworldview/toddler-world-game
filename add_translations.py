def load_data_js():
    with open('data.js', 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 提取 JSON 資料
    categories = ['animals', 'pets', 'ocean', 'vehicles', 'household', 'fruits', 'insects', 'dinosaurs', 'shapes', 'numbers', 'letters']
    data_map = {}
    for cat in categories:
        m = re.search(fr"const\s+{cat}\s*=\s*(\[.*?\]);", content, flags=re.DOTALL)
        if m:
            try:
                data_map[cat] = json.loads(m.group(1))
            except json.JSONDecodeError:
                pass
    return content, data_map

def apply_translations(data_map):
    for cat, items in data_map.items():
        for item in items:
            orig_name = item.get('name', '')
            if 'name_zh' not in item:
                item['name_zh'] = orig_name
            
            # 從對照表取得翻譯，若找不到則預設填入原始中文
            trans = translations.get(orig_name, {"en": orig_name, "ja": orig_name})
            item['name_en'] = trans['en']
            item['name_ja'] = trans['ja']
            
            # 從根層移除舊有 'name' 屬性，全面強制三語屬性
            if 'name' in item:
                del item['name']
    return data_map

def save_data_js(content, data_map):
    for cat, items in data_map.items():
        # 將舊有宣告區塊替換為包含多語系的新陣列
        json_str = json.dumps(items, ensure_ascii=False, indent=4)
        content = re.sub(fr"const\s+{cat}\s*=\s*\[.*?\];", f"const {cat} = {json_str};", content, flags=re.DOTALL)
    
    with open('data.js', 'w', encoding='utf-8') as f:
        f.write(content)

if __name__ == '__main__':
    orig_content, data_map = load_data_js()
    updated_map = apply_translations(data_map)
    save_data_js(orig_content, updated_map)
    print("SUCCESS: 350+ entries extended to trilingual format!")
